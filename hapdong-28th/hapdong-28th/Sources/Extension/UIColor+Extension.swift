import UIKit

extension UIColor {
    
    @nonobjc class var rightGray: UIColor {
      return UIColor(white: 174.0 / 255.0, alpha: 1.0)
    }

  @nonobjc class var mustard: UIColor {
    return UIColor(red: 202.0 / 255.0, green: 166.0 / 255.0, blue: 90.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var darkGray: UIColor {
    return UIColor(white: 61.0 / 255.0, alpha: 1.0)
  }

  

  @nonobjc class var middleGray: UIColor {
    return UIColor(white: 109.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var transparentGray: UIColor {
    return UIColor(red: 67.0 / 255.0, green: 69.0 / 255.0, blue: 69.0 / 255.0, alpha: 0.71)
  }

  @nonobjc class var whiteGray: UIColor {
    return UIColor(white: 230.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var redOrange: UIColor {
    return UIColor(red: 236.0 / 255.0, green: 99.0 / 255.0, blue: 93.0 / 255.0, alpha: 1.0)
  }
    

}
